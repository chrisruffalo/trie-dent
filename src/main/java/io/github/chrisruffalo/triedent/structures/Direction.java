package io.github.chrisruffalo.triedent.structures;

public enum Direction {

    /**
     * The value is lower (left) value
     */
    LOWER,

    /**
     * The value is higher (right) value
     */
    HIGHER,

    /**
     * The value is equal (center) value
     */
    CENTER,

    /**
     * No base value given, meaning there is no value in the current node
     */
    NONE,

    ;

}
